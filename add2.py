import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    #                     #
    #        TODO:        #
    #                     #
    #######################
    cur1 = l1
    cur2 = l2
    l1_number = ''
    l2_number = ''
    new_l1_num = ''
    new_l2_num = ''
    while cur1 is not None:
        num1 = cur1.val
        l1_number += str(num1)
        cur1 = cur1.next
    while cur2 is not None:
        num2 = cur2.val
        l2_number += str(num2)
        cur2 = cur2.next
    for i in range(len(l1_number)-1,-1,-1):
        num1 = str(l1_number[i])
        new_l1_num += num1
    for i in range(len(l2_number)-1,-1,-1):
        num2 = str(l2_number[i])
        new_l2_num += num2
    new_num = str(int(new_l1_num)+int(new_l2_num))

    list_node = None
    node = None
    for i in range(len(new_num)-1,-1,-1):
        number = new_num[i]
        if node is None:
        # if list_node is None:
            node = ListNode(int(number),None)
            list_node = node
        else:
            node.next = ListNode(int(number),None)
            node = node.next
    return list_node


